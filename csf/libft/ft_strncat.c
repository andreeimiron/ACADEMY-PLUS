#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t l;
	size_t i;

	i = 0;
	l = 0;
	while (s1[l] != '\0')
	{
		l++;
	}
	while (s2[i] != '\0' && i < n)
	{
		s1[l] = s2[i];
		i++;
		l++;
	}
	s1[l] = '\0';
	return (s1);
}
