#include "libft.h"

void	ft_putnbr(int n)
{
	long	nr;

	nr = n;
	if (nr < 0)
	{
		ft_putchar('-');
		nr = -nr;
	}
	if (nr >= 10)
	{
		ft_putnbr(nr / 10);
		ft_putnbr(nr % 10);
	}
	else
	{
		ft_putchar(nr + '0');
	}
}
