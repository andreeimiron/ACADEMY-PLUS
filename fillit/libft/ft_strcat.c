#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int l;
	int i;

	l = 0;
	i = 0;
	while (s1[l] != '\0')
	{
		l++;
	}
	while (s2[i] != '\0')
	{
		s1[l] = s2[i];
		l++;
		i++;
	}
	s1[l] = '\0';
	return (s1);
}
